#include "3-calc.h"
/**
 * op_add - adds up integers
 * @a: first integer
 * @b: second integer
 * Return: added numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts numbers
 * @a: first integer
 * @b: second integer
 * Return: result of operation
 */
int op_sub(int a, int b)
{
	return (a -  b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: result of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first number
 * @b: ssecond number
 * Return: result of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates mod of two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int op_mod(int a, int b)
{
	return (a % b);

}

